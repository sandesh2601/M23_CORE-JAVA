package com.cg.looping;
//Program on Enhanced For-loop
public class EnchancedForLoopProgram {

	public static void main(String[] args) {
		
	    int[] numbers = {3, 9, 5, -5};
	    
	    // for each loop 
	    for (int number: numbers) {
	      System.out.println(number);
	    }
		
	}

}
